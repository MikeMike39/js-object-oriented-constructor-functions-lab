// Scooter constructor creates a new scooter with three properties
function Scooter(year, color, model) {
  this.year = year;
  this.color = color;
  this.model = model;
}
// Driver constructor creates a new driver with three properties 
function Driver(name, age, experience) {
  this.name = name;
  this.age = age;
  this.experience = experience;
}
// PickupLocation constructor creates a new PickupLocation with two properties
function PickupLocation(address, city){
  this.address = address;
  this.city = city;
}